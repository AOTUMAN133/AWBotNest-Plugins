async def setup(ctx):
    @ctx.on_message(ctx.filters.group & ctx.filters.text, group=7)
    async def _zj_handler(client, message):
        text = (message.text or "").strip()
        if text not in (".zj", ".zhijiao", "掷筊"):
            return

        results = [random.choice(TOSS_OPTIONS) for _ in range(3)]
        symbols = " ".join(TOSS_SYMBOLS[r] for r in results)
        key = "".join(results)
        divination = JIACI_MAP.get(key, f"{key}：此卦无解，随缘即可。")

        reply = (
            f"🔮 <b>掷筊</b>\n\n"
            f"{symbols}\n"
            f"<code>{' '.join(results)}</code>\n\n"
            f"{divination}"
        )

        try:
            await message.edit(reply)
        except Exception:
            await client.send_message(message.chat.id, reply)


